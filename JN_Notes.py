# This is process to use virtual environment in JN without using Conda
# To use a virtual environment with Jupyter Notebook:
# First activate the virtual environment you created.
# You must then pip3 install ipykernel or this process will not work.
# pip3 install ipykernel
# Next create the environment you will use in jupyter notebook
# python3 -m ipykernel install --name=venv_aws --or-- python3 -m ipykernel install -- user --name=venv_aws
# Now you can use the virtual enviroment in jupyter notebook. 
# When you run jupyter notebook select the Kernel you want to use. 
# You should see the name of the virtual envirnment kernel you created in the list of available kernels.Jup             
